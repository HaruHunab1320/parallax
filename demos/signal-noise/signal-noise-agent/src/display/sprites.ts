/**
 * Convert ASCII art to a SpriteFrame. '#' = on, anything else = off.
 * Art must be exactly 40 lines of 32 characters each.
 */
function asciiToSprite(art: string): SpriteFrame {
  const lines = art.split('\n').filter((l) => l.length > 0);
  const height = 40;
  const width = 32;
  const bytesPerRow = width / 8; // 4
  const data = new Uint8Array(bytesPerRow * height);

  for (let row = 0; row < height && row < lines.length; row++) {
    const line = lines[row];
    for (let col = 0; col < width; col++) {
      const ch = col < line.length ? line[col] : '.';
      if (ch === '#') {
        const byteIdx = row * bytesPerRow + (col >> 3);
        const bitIdx = 7 - (col & 7);
        data[byteIdx] |= 1 << bitIdx;
      }
    }
  }

  return { data, width, height };
}

